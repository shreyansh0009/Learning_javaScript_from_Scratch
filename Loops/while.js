// while loop;
// this loop is used in a condition, there is no any starting and ending point as like for.
// in this loop there is a condition, until the condition satisfies the loop will be running.

// e.g.

let a = 8;

while (a !== 0) {
    console.log(a);
    a--;  // decrementing a;
}  // loop will stop when 'a' will be '0';