// 'for' loop, when we know starting and ending point.
// var 'i' is incrementing one by one.
// syntax:
for(start; end; change) {}

for(let i = 0; i <= 10; i++) {
    console.log(i);
}

// now we can use decrement as well, 'i' will decrement one by one.
for(let i = 10; i >= 0; i--) {
    console.log(i);
}